void drawHeartSpO2(int countdownSeconds, bool showFinal, int animatedHeartRate, bool showHeartRate)
{
   
   u8g2.drawXBMP(112, 0, 16, 16, wifi_final_bits);

   char buf[32];
   u8g2.enableUTF8Print();
   u8g2.setFont(u8g2_font_wqy16_t_gb2312);

   if (showFinal) {
      
      u8g2.drawFrame(4, 16, 120, 44);
      sprintf(buf, "平均心率");
      int x = (128 - u8g2.getUTF8Width(buf)) / 2;
      if (x < 0) x = 0;
      u8g2.drawUTF8(x, 34, buf); 

      if (showHeartRate) {
         if (animatedHeartRate > 0) {
            sprintf(buf, "%d BPM", animatedHeartRate);
         } else {
            sprintf(buf, "-- BPM");
         }
      } else {
         sprintf(buf, "   ");
      }
      int x2 = (128 - u8g2.getUTF8Width(buf)) / 2;
      if (x2 < 0) x2 = 0;
      u8g2.drawUTF8(x2, 52, buf); 
      return;
   }

   if (countdownSeconds > 0) {
      sprintf(buf, "倒计时：%ds", countdownSeconds);
      int x = (128 - u8g2.getUTF8Width(buf)) / 2;
      if (x < 0) x = 0;
      u8g2.drawUTF8(x, 54, buf); 
   } else {
      sprintf(buf, "测量中...");
      int x = (128 - u8g2.getUTF8Width(buf)) / 2;
      if (x < 0) x = 0;
      u8g2.drawUTF8(x, 54, buf); 
   }

   if (MAX30102_State.valid)
   {
      sprintf(buf, "心率：%d BPM", MAX30102_State.heartRate);
   }
   else
   {
      sprintf(buf, "心率：-- BPM");
   }
   int x = (128 - u8g2.getUTF8Width(buf)) / 2;
   if (x < 0) x = 0;
   u8g2.drawUTF8(x, 32, buf);
}

